exp as (
  select r.id as rid,
         t.elem as elem, t.ord as ord,
         (select gg.grams from g gg
            where lower(trim(gg.recipe_name)) = lower(trim(r.name_en))
              and lower(trim(gg.item_name))   = lower(trim(t.elem->>'name_en'))
            limit 1) as new_grams
  from public.recipes r
  cross join lateral jsonb_array_elements(r.items) with ordinality as t(elem, ord)
  where jsonb_typeof(r.items) = 'array'
),
rebuilt as (
  select rid,
         jsonb_agg(
           case when new_grams is not null and (elem->>'grams') is null
                then jsonb_set(elem, '{grams}', to_jsonb(new_grams))
                else elem end
           order by ord
         ) as items
  from exp group by rid
)
